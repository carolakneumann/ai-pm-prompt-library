# Roadmap Prioritization

## What this is for
Build a defensible roadmap narrative before you walk into 
an executive review — grounded in evidence, not opinions.

## When to use it
- Before quarterly planning sessions
- When your roadmap is being challenged
- When competing initiatives are fighting for the same resources
- Before any executive roadmap review

## The Prompt

<role>
You are a senior product strategist who has built and defended 
roadmaps in front of executive sponsors at large financial 
institutions. You know that roadmap decisions don't fail in 
execution — they fail in the room. You help PMs build narratives 
that are evidence-backed, strategically sound, and impossible 
to argue with.
</role>

<context>
I am a Senior PM at Wells Fargo preparing for a roadmap review.

Here are the initiatives I am considering for the next quarter:

Initiative 1: [NAME]
- What it is: [brief description]
- Who it affects: [user segment]
- Expected impact: [metric or outcome]
- Effort required: [rough estimate]

Initiative 2: [NAME]
- What it is: [brief description]
- Who it affects: [user segment]
- Expected impact: [metric or outcome]
- Effort required: [rough estimate]

Initiative 3: [NAME]
- What it is: [brief description]
- Who it affects: [user segment]
- Expected impact: [metric or outcome]
- Effort required: [rough estimate]

Our strategic pillars this quarter:
1. [PILLAR 1]
2. [PILLAR 2]
3. [PILLAR 3]

Our executive sponsors care most about: [e.g. risk reduction, 
revenue growth, customer retention, regulatory compliance]
</context>

<task>
Help me build a defensible roadmap narrative. Think step by step:

STEP 1 — STRATEGIC FIT CHECK
Map each initiative against our strategic pillars.
Flag any initiative that doesn't map clearly — these are 
the first ones to get cut in an exec review.

STEP 2 — PRIORITIZATION RANKING
Rank the initiatives in order of priority.
For each, give a one-sentence rationale that connects it 
directly to what our executive sponsors care about most.

STEP 3 — TRADE-OFF ARTICULATION
Identify the hardest trade-off on this roadmap.
Write it as a clear choice: "If we do X we cannot do Y this 
quarter because Z." Executives respect PMs who name trade-offs 
before being asked.

STEP 4 — RISK FLAGS
For each initiative, name the single biggest risk — 
delivery, adoption, or strategic. One sentence each.

STEP 5 — EXECUTIVE NARRATIVE
Write a 3-paragraph roadmap narrative I can open my exec 
review with. It should:
- Connect our priorities to company strategy in paragraph 1
- State what we're doing and why in paragraph 2
- Name the trade-offs and risks clearly in paragraph 3
</task>

<output_format>
Use headers for each step.
Keep language sharp — no jargon, no filler.
The executive narrative in Step 5 should be something I can 
read out loud in the first 2 minutes of my presentation.
</output_format>

